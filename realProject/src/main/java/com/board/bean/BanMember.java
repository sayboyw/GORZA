package com.board.bean;

import java.sql.Date;
//회원 정지
public class BanMember {

	private String id;
	private String nickName;
	private String intro;
	private String email;
	
	//정지관련
	private int ban_num;;
	private Date ban_start;
	private Date ban_stop;
	private int bcnt;
	private String ban_reason;
	private int ban_leave;
	
	//기업회원 정지관련
	private String c_name;
	
	//날짜 비교값
	private int datechk;

	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public int getBan_num() {
		return ban_num;
	}

	public void setBan_num(int ban_num) {
		this.ban_num = ban_num;
	}

	public Date getBan_start() {
		return ban_start;
	}

	public void setBan_start(Date ban_start) {
		this.ban_start = ban_start;
	}

	public Date getBan_stop() {
		return ban_stop;
	}

	public void setBan_stop(Date ban_stop) {
		this.ban_stop = ban_stop;
	}

	public int getBcnt() {
		return bcnt;
	}

	public void setBcnt(int bcnt) {
		this.bcnt = bcnt;
	}

	public String getBan_reason() {
		return ban_reason;
	}

	public void setBan_reason(String ban_reason) {
		this.ban_reason = ban_reason;
	}

	public int getBan_leave() {
		return ban_leave;
	}

	public void setBan_leave(int ban_leave) {
		this.ban_leave = ban_leave;
	}

	public int getDatechk() {
		return datechk;
	}

	public void setDatechk(int datechk) {
		this.datechk = datechk;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getC_name() {
		return c_name;
	}

	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	
	
}
